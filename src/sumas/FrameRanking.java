    private void generarTabla() {
        Collections.sort(this.sistema.getJugadores());
        UneditableModel jugadores = new UneditableModel();
        jugadores.addColumn("Nombre");
        jugadores.addColumn("Alias");
        jugadores.addColumn("Edad");
        jugadores.addColumn("Puntaje");
        for (Jugador j : this.sistema.getJugadores()) {
            Object[] data = {j.getNombre(), j.getAlias(), j.getEdad(), j.getPuntaje()};
            jugadores.addRow(data);
        }
        this.tablaJugadores.setModel(jugadores);
    }

